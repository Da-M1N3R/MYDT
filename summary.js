/* ----------  inject into columns ---------- */
function inject(list, targetId) {
  const box = document.getElementById(targetId);
  list.forEach(({ arc, story }) => {
    const details = document.createElement("details");
    details.innerHTML = `
      <summary>${arc}</summary>
      <p>${story}</p>
    `;
    box.appendChild(details);
  });
}
